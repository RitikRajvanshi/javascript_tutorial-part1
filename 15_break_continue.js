// break keyword

for(let i=0; i<=10; i++){
    if(i==7){
        break;
    }
    console.log(i)
}
 console.log("Loop is break after 7")

//continue keyword

for(let i=0; i<=10; i++){
    if(i==7){
        continue;
    }
    console.log(i)
}
 console.log("Loop is continue skipping iteration at 7")