// boolean & comparison operator


//boolean have two value true(1) or false(0)
let num1 = 25;
let num2 = 27;
console.log(num1 > num2);
console.log(num1 < num2);

// == vs ===

//=== strict equality operator

// == compares only values.
// === compare values as well as type of the operands also.

let num3 = "25";
let num4 = 25;
console.log(num3 == num4);
//but
console.log(num3 === num4);

// != vs !==

console.log(num3 != num4);
console.log(num3 !== num4);



